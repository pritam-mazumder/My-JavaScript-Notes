/*
    = :     ASSIGNMENT OPERATOR
    == :    IDENTITY --> loose equality (double equals)
    === :   EQUALITY --> strict equality (triple equals)
*/


console.log(0 == 0);
console.log(0 == 0.0);
console.log(0 == '0');
console.log(0 == '');
console.log(0 == false);
console.log(null == null);
console.log(null == undefined);

console.log('\n')

console.log(0 === 0);
console.log(0 === 0.0);
console.log(0 === '0');
console.log(0 === '');
console.log(0 === false);
console.log(null === null);
console.log(null === undefined);