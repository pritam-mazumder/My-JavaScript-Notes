// Sum of two numbers

function sum(num1, num2) {
  return num1 + num2;
}

console.log(sum(3, 5));
console.log(sum(4, 2));

// Convert Fahrenheit to Celsius:

function toCelsius(fahrenheit) {
  return (5 / 9) * (fahrenheit - 32);
}

let value = toCelsius(77);
console.log(value);

// Average of two
function avg(n1, n2) {
  return (n1 + n2) / 2;
}

console.log(avg(2, 3));
